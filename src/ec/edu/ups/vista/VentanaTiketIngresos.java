    public void CrearFactura(){

        local = LocalDateTime.now();
        String estado="deuda";
        
        Calendar fechaIngreso = Calendar.getInstance();

        Sitioss sitio=this.controladorSitio.buscar(numero);
        System.out.println(sitio);
        Vehiculos vehiculo = sitio.getVehiculos();
        Clientes cliente=vehiculo.getClientes();
        
        Facturas factura=new Facturas(fechaIngreso, estado, cliente, sitio);
        boolean respuesta= controladorFactura.crear(factura);
        
        String datos = "\nFecha y hora de ingreso: "+fechaIngreso.getTime()+"\nCliente: " +cliente.getNombre()+" "+cliente.getApellido()+"\nCedula: "+cliente.getCedula()+"\nPlaca vehiculo: "+vehiculo.getPlaca()+"\nMarca: "+vehiculo.getMarca()+"\nModelo: "+vehiculo.getModelo()+"\nNumero sitio parqueado: "+sitio.getId();
        txtDatos.setText(datos);
        JOptionPane.showMessageDialog(this, "Operaci√≥n : " + respuesta);
    }

