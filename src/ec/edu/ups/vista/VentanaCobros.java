    private void btnCobrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCobrarActionPerformed
        long numero=Long.parseLong(txtNumero.getText());
 
        Facturas factura= controladorFactura.buscar(numero);
        txtTotal.getText();
        Clientes cliente = factura.getClientes();
        Sitioss sitio= factura.getSitios();
        //float cantidad=(float) Double.parseDouble(txtTotal.getText());

        Sitioss sitios = controladorSitio.buscar(sitio.getId());
        sitios.setEstado("Libre");
        sitios.setVehiculos(null);
        controladorSitio.actualizar(sitios);

        String total= txtTotal.getText().replace(",", ".");
        double totalDouble=Double.parseDouble(total);

        Facturas factura1= new Facturas(numero, factura.getFechaYHoraDeIngreso(), JDateSalida.getCalendar(), "Pagado", totalDouble, cliente, sitio);
        controladorFactura.actualizar(factura1);
        JOptionPane.showMessageDialog(this, "Gracias vuelva pronto");
        limpira();
        this.setVisible(false);
    }//GEN-LAST:event_btnCobrarActionPerformed


