    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int fila = tblClientes.getSelectedRow();
        long cod = (long) tblClientes.getValueAt(fila, 0);
        String cedula = (String)tblClientes.getValueAt(fila, 1);
        String nombre = (String) tblClientes.getValueAt(fila, 2);
        String Apelldio = (String) tblClientes.getValueAt(fila, 3);
        Calendar fecha = (Calendar) tblClientes.getValueAt(fila, 4);

        txtCodigo.setText(String.valueOf(cod));
        txtCedula.setText(cedula);
        txtNombre.setText(nombre);
        txtApellido.setText(Apelldio);
        JDate.setCalendar(fecha);
    }//GEN-LAST:event_tblClientesMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean resultado = false;
        long codigo=Long.parseLong(txtCodigo.getText());
        String cedula= txtCedula.getText();
        String nombre= txtNombre.getText();
        String Apellido=txtApellido.getText();
        Calendar fecha = JDate.getCalendar();

        //Cliente cl = new Cliente(cedula);
        if (cedula.isEmpty() || nombre.isEmpty() || Apellido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados");
        }else{
            if(controladorCliente.buscar(codigo)!= null){
                Clientes cliente = controladorCliente.buscar(codigo);
                cliente.setCedula(cedula);
                cliente.setNombre(nombre);
                cliente.setApellido(Apellido);
                cliente.setFechaNacimiento(fecha);
                resultado =controladorCliente.actualizar(cliente);
            }else{
                Clientes cliente = new Clientes(cedula, nombre, Apellido, fecha);
                resultado =controladorCliente.crear(cliente);
            }
            limpiar();
            JOptionPane.showMessageDialog(this, "Operación : " + resultado);
            cargarDatos();
            dispose();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int eliminar = JOptionPane.showConfirmDialog(this, "¿Seguro quieres eliminar este Alumno?");
        long codigo=Long.parseLong(txtCodigo.getText());
        
        Clientes cliente= controladorCliente.buscar(codigo);
        if(eliminar == JOptionPane.YES_OPTION){
            boolean resultado = controladorCliente.eliminar(cliente);
            JOptionPane.showMessageDialog(this, "Operación : " + resultado);
            limpiar();
            cargarDatos();
            dispose();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

