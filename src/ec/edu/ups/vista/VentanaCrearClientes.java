    public void cargarDatos() {
       DefaultTableModel modeloTabla = (DefaultTableModel) tblClientes.getModel();

        modeloTabla.setRowCount(0);
        for (Clientes cliente : controladorCliente.findAll()) {
            Object[] rowData = {cliente.getId(),cliente.getCedula(), cliente.getNombre(),cliente.getApellido(),cliente.getFechaNacimiento()};
            modeloTabla.addRow(rowData);
        }
        tblClientes.setModel(modeloTabla);
    }
