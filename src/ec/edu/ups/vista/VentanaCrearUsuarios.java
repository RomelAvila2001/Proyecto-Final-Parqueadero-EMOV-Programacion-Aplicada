    public void cargarDatos() {
       DefaultTableModel modeloTabla = (DefaultTableModel) tblUsuarios.getModel();

        modeloTabla.setRowCount(0);
        for (Usuarios usuario : controladorUsuario.findAll()) {
            Object[] rowData = {usuario.getId(),usuario.getCedula(), usuario.getNombre(),usuario.getApellido(),usuario.getFechaNacimiento(),usuario.getCorreo(),usuario.getContraseña()};
            modeloTabla.addRow(rowData);
        }
        tblUsuarios.setModel(modeloTabla);
    }
