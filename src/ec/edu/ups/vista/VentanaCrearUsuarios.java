    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        int fila = tblUsuarios.getSelectedRow();
        long cod = (long) tblUsuarios.getValueAt(fila, 0);
        String cedula = (String)tblUsuarios.getValueAt(fila, 1);
        String nombre = (String) tblUsuarios.getValueAt(fila, 2);
        String Apelldio = (String) tblUsuarios.getValueAt(fila, 3);
        Calendar fecha = (Calendar) tblUsuarios.getValueAt(fila, 4);
        String correo = (String) tblUsuarios.getValueAt(fila, 5);
        String contrase = (String) tblUsuarios.getValueAt(fila, 6);

        txtCodigo.setText(String.valueOf(cod));
        txtCedula.setText(cedula);
        txtNombre.setText(nombre);
        txtApellido.setText(Apelldio);
        JDate.setCalendar(fecha);
        txtCorreo.setText(correo);
        txtContrase.setText(contrase);
    }//GEN-LAST:event_tblUsuariosMouseClicked

