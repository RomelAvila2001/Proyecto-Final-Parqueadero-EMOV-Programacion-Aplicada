    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean resultado = false;
        long codigo=Long.parseLong(txtCodigo.getText());
        String cedula= txtCedula.getText();
        String nombre= txtNombre.getText();
        String Apellido=txtApellido.getText();
        Calendar fecha = JDate.getCalendar();
        String correo=txtCorreo.getText();
        String contra=txtContrase.getText();
        try{
            Constructor<Usuarios> constructor = Usuarios.class.getDeclaredConstructor();
            constructor.setAccessible(true);
            Usuarios u = constructor.newInstance();
            u.setId(codigo);
            if (cedula.isEmpty() || nombre.isEmpty() || Apellido.isEmpty() || correo.isEmpty() || contra.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados");
            } else {
                if (controladorUsuario.buscar(codigo) != null) {
                    String encriptarContraseña=controladorUsuario.ecriptarContraseña(KeyUnlock, contra);
                    u.setId(codigo);
                    u.setCedula(cedula);
                    u.setNombre(nombre);
                    u.setApellido(Apellido);
                    u.setFechaNacimiento(fecha);
                    u.setCorreo(correo);
                    u.setContraseña(encriptarContraseña);
                    resultado=controladorUsuario.actualizar(u);
                }else{
                    String encriptarContraseña=controladorUsuario.ecriptarContraseña(KeyUnlock, contra);

                    u.setCedula(cedula);
                    u.setNombre(nombre);
                    u.setApellido(Apellido);
                    u.setFechaNacimiento(fecha);
                    u.setCorreo(correo);
                    u.setContraseña(encriptarContraseña);
                    resultado=controladorUsuario.crear(u);
                }
                limpiar();
                JOptionPane.showMessageDialog(this, "Operación : " + resultado);
                cargarDatos();
                dispose();
            }
        } catch (NoSuchMethodException ex) {
            Logger.getLogger(VentanaCrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SecurityException ex) {
            Logger.getLogger(VentanaCrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InstantiationException ex) {
            Logger.getLogger(VentanaCrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalAccessException ex) {
            Logger.getLogger(VentanaCrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalArgumentException ex) {
            Logger.getLogger(VentanaCrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvocationTargetException ex) {
            Logger.getLogger(VentanaCrearUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

